#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	s_len;
	char	*str;

	if (!s || !f)
		return (NULL);
	s_len = ft_strlen(s);
	str = (char *)malloc(sizeof(char) + (s_len));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
